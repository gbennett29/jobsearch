#!/usr/bin/env node
/**
 * Reads .env and writes firebase-config.js for static hosting / live-server.
 * Run from repo root: node scripts/generate-firebase-config.mjs
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const envPath = path.join(root, ".env");
const outPath = path.join(root, "firebase-config.js");

const ENV_KEYS = [
  ["FIREBASE_API_KEY", "apiKey"],
  ["FIREBASE_AUTH_DOMAIN", "authDomain"],
  ["FIREBASE_PROJECT_ID", "projectId"],
  ["FIREBASE_STORAGE_BUCKET", "storageBucket"],
  ["FIREBASE_MESSAGING_SENDER_ID", "messagingSenderId"],
  ["FIREBASE_APP_ID", "appId"],
];

function parseEnv(text) {
  const env = {};
  for (const line of text.split("\n")) {
    const t = line.trim();
    if (!t || t.startsWith("#")) continue;
    const eq = t.indexOf("=");
    if (eq === -1) continue;
    const key = t.slice(0, eq).trim();
    let val = t.slice(eq + 1).trim();
    if (
      (val.startsWith('"') && val.endsWith('"')) ||
      (val.startsWith("'") && val.endsWith("'"))
    ) {
      val = val.slice(1, -1);
    }
    env[key] = val;
  }
  return env;
}

if (!fs.existsSync(envPath)) {
  console.error("Missing .env — copy .env.example to .env and add your Firebase web config values.");
  process.exit(1);
}

const env = parseEnv(fs.readFileSync(envPath, "utf8"));
const config = {};
for (const [envKey, jsKey] of ENV_KEYS) {
  const v = env[envKey];
  if (!v) {
    console.error(`Missing ${envKey} in .env`);
    process.exit(1);
  }
  config[jsKey] = v;
}

const banner = `/* Generated by scripts/generate-firebase-config.mjs — do not commit. */\n`;
const body = `window.__firebaseConfig = ${JSON.stringify(config, null, 2)};\n`;
fs.writeFileSync(outPath, banner + body, "utf8");
console.log("Wrote", outPath);
